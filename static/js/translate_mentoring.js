// ========== Runtime translator ==========
const langToggle = document.getElementById("langToggle");
let currentLang = "fa";

function translatePage(lang) {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            // Handle both text and HTML content
            if (el.tagName === 'UL' || el.tagName === 'OL') {
                el.innerHTML = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });
    
    // Update document direction and language toggle
    document.documentElement.dir = lang === "fa" ? "rtl" : "ltr";
    langToggle.textContent = lang === "fa" ? "EN" : "FA";
    
    // Special handling for buttons with icons
    document.querySelectorAll(".btn-apply").forEach(btn => {
        const originalText = lang === "fa" ? "همین حالا شروع کنید" : "Get Started Now";
        btn.innerHTML = btn.textContent.includes(originalText) 
            ? (lang === "fa" ? "همین حالا شروع کنید" : "Get Started Now")
            : btn.innerHTML;
    });
}

// Language toggle event listener
langToggle.addEventListener("click", () => {
    currentLang = currentLang === "fa" ? "en" : "fa";
    translatePage(currentLang);
});

// Initial translation
translatePage(currentLang);