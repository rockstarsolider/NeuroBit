let currentLang = localStorage.getItem('neurobit-lang') || 'fa';

function applyTranslations(pageKey) {
    const pageTranslations = translations[pageKey];
    if (!pageTranslations) {
        console.error(`No translations found for page key: ${pageKey}`);
        return;
    }

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (pageTranslations[currentLang] && pageTranslations[currentLang][key]) {
            el.innerHTML = pageTranslations[currentLang][key];
        }
    });

    document.documentElement.dir = currentLang === "fa" ? "rtl" : "ltr";
    document.documentElement.lang = currentLang;

    document.querySelectorAll(".lang-toggle").forEach(btn => {
        btn.textContent = currentLang === "fa" ? "EN" : "FA";
    });

    // به‌روزرسانی عنوان صفحه در صورت وجود کلید مخصوص
    const titleKey = `${pageKey}_title`;
    if (pageTranslations[currentLang] && pageTranslations[currentLang][titleKey]) {
        document.title = `NEUROBIT – ${pageTranslations[currentLang][titleKey]}`;
    }
}

/**
 * زبان را تغییر داده و ترجمه‌ها را مجدداً اعمال می‌کند
 * @param {string} pageKey - کلید شناسایی صفحه
 */
function toggleLanguage(pageKey) {
    currentLang = currentLang === "en" ? "fa" : "en";
    localStorage.setItem('neurobit-lang', currentLang);
    applyTranslations(pageKey);
}

/**
 * --- راه‌انداز اصلی ---
 * این بخش به صورت خودکار پس از بارگذاری کامل DOM اجرا می‌شود
 */
document.addEventListener('DOMContentLoaded', () => {
    // ۱. کلید صفحه را از تگ body بخوان
    const pageKey = document.querySelector('main').dataset.pageKey;

    // ۲. بررسی کن که کلید وجود دارد
    if (!pageKey) {
        console.error('Error: The data-page-key attribute is not set on the <body> tag.');
        return; // اگر کلیدی تعریف نشده باشد، اجرا را متوقف کن
    }

    // ۳. ترجمه اولیه را اعمال کن
    applyTranslations(pageKey);

    // ۴. شنونده‌های رویداد (Event Listeners) را برای دکمه‌های زبان تنظیم کن
    document.querySelectorAll(".lang-toggle").forEach(btn => {
        btn.addEventListener("click", () => toggleLanguage(pageKey));
    });
});